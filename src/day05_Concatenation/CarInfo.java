package day05_Concatenation;

public class CarInfo {

    public static void main(String[] args) {

        int year = 2018;
        String make = "Toyota";
        String model = "Camry";
        int miles = 50000;
        String color = "Red";
        int price = 19000;

        System.out.println(year + " " + make + " " + model + "," + " " + miles + "," + " " + color + "," + " " + "$" + price);
    }
}

