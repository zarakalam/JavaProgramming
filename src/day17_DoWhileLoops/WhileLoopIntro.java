package day17_DoWhileLoops;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if(true){
            System.out.println("Wooden Spoon"); // executed 1 time only
        }


        System.out.println("======================");

        while(true){
            System.out.println("Wooden Spoon"); // executes infinite times
        }
    }
}
