package day18_NestedLoop;

public class NestedLoopPracticeTwo6th {
    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {  // this creates 10 lines

            for (int i = 0; i < 10; i++) {
                System.out.print("* ");  //this creates the stars for the pic
            }
            System.out.println();


        }



    }
}

