package day15_ForLoop;

public class ForLoopMakeAShapePractice {
    public static void main(String[] args) {

        for(int i = 1; i <= 8 ; i +=1){
            System.out.println("\t\t\t\t\t\t\t" + "* * * * * *");
        }
    }
}
