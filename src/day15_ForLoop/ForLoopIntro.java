package day15_ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {

        // for(initialization; condition ; Iteration){}
        // i stands for initialization

        System.out.println("Hello World!");
        System.out.println("======================");

        for(int i = 0; i <= 15; i ++){
            System.out.println("Hello World!");
        }




    }
}
