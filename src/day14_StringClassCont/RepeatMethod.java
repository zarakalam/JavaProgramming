package day14_StringClassCont;

public class RepeatMethod {
    public static void main(String[] args) {

        //String Repeat Method:

        String str = "Cydeo"; //if you want to add a space after the repeat add a space after the O

        String str1= str.repeat(4);
        System.out.println("str1 = " + str1);

        String str2 = "Wooden Spoon"; // if you add a space after the repeat add a space after the N
        String str3 = str2.repeat(100);
        System.out.println("str3 = " + str3);

        System.out.println("==================================");



    }
}
